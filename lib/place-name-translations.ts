/**
 * Translate a place friendly_id to Dutch.
 * Strips trailing disambiguation numbers (e.g. "Syria 1" → "Syrië").
 * Returns the original name if no translation is found.
 */
export function translatePlaceName(friendlyId: string): string {
  // Strip trailing " 1", " 2", " 3", etc.
  const base = friendlyId.replace(/\s+\d+$/, '').trim();
  return PLACE_NAME_NL[base] ?? PLACE_NAME_NL[friendlyId] ?? friendlyId;
}
